//Operadores relacionais sempre vai comparar dois numeros,  transformar em bool

int a = 10;
int b = 11;

//Maior
Console.WriteLine(a > b);

//Maior ou igual
Console.WriteLine(a >= b);

//Menor
Console.WriteLine(a < b);

//Menor ou igual
Console.WriteLine(a <= b);

//Igualdade
Console.WriteLine(a == b);

//Diferente
Console.WriteLine(a != b);

//Armaznar valor boleano
bool booleano = a != b;
Console.WriteLine(booleano);